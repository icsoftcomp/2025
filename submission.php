						<div class="section-header">
                            <h2> Anti-Plagiarism Policy: </h2>
                        </div>
                        <div class="about-text">
						<p>
							icSoftComp2023 is intended to accept ONLY high level quality papers which have originality and novelty in content. The conference strongly suggest NOT to use or copy any part of the already published materials. The papers submitted to the conference first will undergo screening process for originality in content. If they satisfy this requirement the papers will be further sent to review. Otherwise the papers will be rejected at the first level. The authors who indulge in plagiarism will be blacklisted.
