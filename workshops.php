							Submission guidelines </b>
							<br><br>
Each icSoftComp2023 workshop proposal / special sessions has to be submitted in a single PDF file, and must include:
<br>
- Name and abbreviation of the workshop.
<br>
- Names, addresses, and a short (200 words) bio of the Organizing Committee members.
<br>
- A brief description of the technical focus addressed by the workshop/Special session, reasons why the workshop/Special session is of interest at this time, and reasons why this area is important in the context of icSoftComp2023.
<br>
- The planned format of the workshop/Special session, including a strategy to facilitate lively and interactive discussions.
<br>
- If applicable, a description of past events of the workshop/Special session, including statistics of submitted/accepted papers and attendee numbers. If the workshop/Special session does not have past editions, please provide estimates and explain a strategy on how to reach the targets of paper submissions and attendees.
<br>
- Publicity and advertising plan to attract paper submissions and attendees.
<br>
- A draft of 1-page "Call for Papers" including title, description, topics and important dates. 
<br><br>
