The 2023 5th International Conference on Soft Computing and its Engineering Applications (icSoftComp2023) invites proposals for full-day workshops or half-day special sessions. icSoftComp2023 will take place on December 07-09, 2023 in Changa, India. It is the premier forum for technical exchange on soft computing and its engineering applications. The purpose of the workshops / special sessions is to complement the conference program with in-depth forums that are dedicated to a specific topic related to scope of icSoftComp2023. Proposals from industry and academia are welcome. Workshops / Special sessions will have their papers published in the conference proceedings. 
                            </p>
                            
